namespace FolderBrowser.Themes
{
	using System.Windows;

	/// <summary>
	/// Defines the resource key that are available for styling, colors, and brushes
	/// in this application.
	/// </summary>
	public static class ResourceKeys
	{
		/// <summary>
		/// Resource key of the folder browser drop down normal background brush key.
		/// </summary>
		public static readonly ComponentResourceKey DropDownBackgroundKey = new ComponentResourceKey(typeof(ResourceKeys), "DropDownBackgroundKey");

		/// <summary>
		/// Resource key of the folder browser dialogs normal background brush key.
		/// </summary>
		public static readonly ComponentResourceKey DialogBackgroundKey = new ComponentResourceKey(typeof(ResourceKeys), "DialogBackgroundKey");

		/// <summary>
		/// Resource key of the treeview normal background brush key.
		/// </summary>
		public static readonly ComponentResourceKey TreeViewBackgroundKey = new ComponentResourceKey(typeof(ResourceKeys), "TreeViewBackgroundKey");

		/// <summary>
		/// Resource key of the folder browser dialogs normal foreground brush key.
		/// </summary>
		public static readonly ComponentResourceKey DialogForegroundKey = new ComponentResourceKey(typeof(ResourceKeys), "DialogForegroundKey");
	}
}
